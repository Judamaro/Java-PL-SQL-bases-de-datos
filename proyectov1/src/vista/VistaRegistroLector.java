    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser j = new JFileChooser();
        FileNameExtensionFilter fil = new FileNameExtensionFilter("JPG, PNG & GIF", "jpg", "png", "gif");
        j.setFileFilter(fil);

        int s = j.showOpenDialog(this);
        if (s == JFileChooser.APPROVE_OPTION) {
            String ruta = j.getSelectedFile().getAbsolutePath();
            jTextField12.setText(ruta);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nomb1 = jTextField1.getText();
        String nomb2 = jTextField3.getText();
        String apell1 = jTextField2.getText();
        String apell2 = jTextField4.getText();
        String dirr = jTextField5.getText();
        String tel1 = jTextField6.getText();
        String tel2 = jTextField7.getText();
        String tel3 = jTextField8.getText();
        String tel4 = jTextField9.getText();
        File ruta = new File(jTextField12.getText());
        String nickname = jTextField10.getText();
        String contra = jTextField11.getText();

        if (!nomb1.equals("") && !apell1.equals("") && !dirr.equals("") && !tel1.equals("") && !tel2.equals("") && !tel3.equals("") && !nickname.equals("") && !contra.equals("") && jTextField12.getText().isEmpty()) {
            ControlLector objcl = new ControlLector();

            boolean t = objcl.registrarlector(nomb1, nomb2, apell1, apell2, dirr, tel1, tel2, tel3, tel4, ruta, nickname, contra);

            if (t == true) {
                JOptionPane.showMessageDialog(null, "Registro con exitoso");
            } else {
                JOptionPane.showMessageDialog(null, "Por favor revise los datos requeridos");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor revisar los datos obligatorios");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

